    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        animal = animalDao.findById(Integer.parseInt(input_id.getText()));
        ClientDao clientDao = DaoFactory.createClientDao();
        
        nome_on.setText(animal.getName());
        id_on.setText(Integer.toString(animal.getId()));
        especie_on.setText(animal.getSpecies());
        
        client = clientDao.findById(animal.getOwner());
        
        dono_on.setText(client.getName());
        
    }//GEN-LAST:event_jButton1ActionPerformed

