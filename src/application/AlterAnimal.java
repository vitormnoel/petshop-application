    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        try{
            animal.setName(nome.getText());
            animal.setSpecies(especie.getText());
            animal.setOwner(Integer.parseInt(dono.getText()));
            animalDao.update(animal);
            
            JOptionPane.showMessageDialog(null, "Dados atualizados!", "", 1);
            
            input_id.setText("");
            nome.setText("");
            especie.setText("");
            dono.setText("");
            
            nome_on.setText("");
            especie_on.setText("");
            id_on.setText("");
            dono_on.setText("");
            
        }catch(NullPointerException e){
            JOptionPane.showMessageDialog(null, "Não foi possivel atualizar.", "ERRO", 0);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

