    private void alter_prodActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_alter_prodActionPerformed
         try{
               product.setName(input_name.getText());
               product.setamount(Integer.parseInt(input_amount.getText()));

               productsDao.update(product);
               JOptionPane.showMessageDialog(null, "Produto alterado!", "", 1);
         }catch(Exception e){
             JOptionPane.showMessageDialog(null, "Nenhum produto alterado.", "ERRO", 0);
         }
    }//GEN-LAST:event_alter_prodActionPerformed

