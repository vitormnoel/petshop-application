    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        AnimalDao animalDao = DaoFactory.createAnimalDao();
        Animal animal = new Animal();
        animal.setName(input_name.getText());
        animal.setId(Integer.parseInt(input_id.getText()));
        animal.setOwner(Integer.parseInt(input_owner.getText()));
        animal.setSpecies(input_especies.getText());
        
        animalDao.insert(animal);
        
        JOptionPane.showMessageDialog(null, "Animal cadastrado com sucesso!", "", 1);
        
        input_name.setText("");
        input_owner.setText("");
        input_id.setText("");
        input_especies.setText("");
    }//GEN-LAST:event_jButton1ActionPerformed

