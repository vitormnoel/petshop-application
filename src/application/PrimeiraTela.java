    private void button_signinActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_signinActionPerformed
        ClientDao clientDao = DaoFactory.createClientDao();
        Client client = new Client();
        
        client = clientDao.findByEmail(text_email.getText(), text_password.getText());
        
        try{
           if(text_email.getText().equals(client.getEmail())){
               JOptionPane.showMessageDialog(null, "Logado", "LOGIN", 1);
               
               dispose();
               if(client.getId().equals(111222333)){
                   TelaClinica clinica = new TelaClinica();
                   clinica.setVisible(true);
               }
               else{
                   TelaCliente clientScreen = new TelaCliente();
                   clientScreen.Data(client);
                   clientScreen.setVisible(true);
               }
         } 
         }catch(NullPointerException ee){
            JOptionPane.showMessageDialog(null, "Email ou senha incorretos", "ERRO", 0);
         }
          
    }//GEN-LAST:event_button_signinActionPerformed

